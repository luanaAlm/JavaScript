const a = 7
let b = 3

//atribuição aditiva
b += a //b = b + a
console.log(b)

//atribuição subtrativa
b -= 4 //b = b - 4
console.log(b)

//atribuição multiplicativa
b *= 2 //b = b * 3
console.log(b)

//atribuição divisiva
b /= 2 //b = b / 2
console.log(b)

//atribuição modular
b %= 2 //b = b % 2
console.log(b)